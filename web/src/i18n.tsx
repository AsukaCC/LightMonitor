let activeLanguage: Language = readInitialLanguage()

function readInitialLanguage(): Language {
  const stored = localStorage.getItem(languageStorageKey) as Language | null
  if (stored && supportedLanguages.includes(stored)) return stored
  const browser = navigator.language.toLowerCase()
  if (browser.startsWith('ja')) return 'ja-JP'
  if (browser.startsWith('en')) return 'en-US'
  return 'zh-CN'
}

export function translate(key: string, values: TranslationValues = {}): string {
  const translated = activeLanguage === 'ja-JP' ? ja[key] : activeLanguage === 'en-US' ? en[key] : key
  return (translated ?? key).replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`))
}

export function currentLocale() {
  return activeLanguage
}

type I18nValue = {
  language: Language
  setLanguage: (language: Language) => void
  t: Translate
}

const I18nContext = createContext<I18nValue | undefined>(undefined)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(activeLanguage)

  useEffect(() => {
    activeLanguage = language
    localStorage.setItem(languageStorageKey, language)
    document.documentElement.lang = language
  }, [language])

  const value = useMemo<I18nValue>(() => ({
    language,
    setLanguage(next) {
      activeLanguage = next
      setLanguageState(next)
    },
    t: translate,
  }), [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)
  if (!value) throw new Error('useI18n must be used inside I18nProvider')
  return value
}
